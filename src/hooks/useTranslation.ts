export function useTranslation(language: string) {
  return useMemo(() => {
    return (key: string) => {
      if (language === 'ENG') return key;
      if (language === 'RUS') return dictRUS[key] || key;
      if (language === 'GR') return dictGR[key] || key;
      return key;
    };
  }, [language]);
}
